void lcd_5110_Init(LCD_5110_Config*Lcd_InitStructure)
{
	uint8_t lcd_init[6]={0};
	 lcd_init[0] = Function_Base_Address | Extended_Instruction_Mode | Lcd_InitStructure->Opertion_Mode | Lcd_InitStructure->Addressing_Mode;
	 lcd_init[1] = Vop_Base_Address | Lcd_InitStructure->Vop_Value;
	 lcd_init[2] = Temp_Base_Address | Lcd_InitStructure->Temperature_Value;
	 lcd_init[3] = Bias_Base_Address | Lcd_InitStructure->Bias_Value;
	 lcd_init[4] = Function_Base_Address | Basic_Instruction_Mode | Lcd_InitStructure->Opertion_Mode | Lcd_InitStructure->Addressing_Mode;
	 lcd_init[5] = Display_Config_Base_Address | Lcd_InitStructure->Display_Control;

	HAL_GPIO_WritePin(RST_PORT, RST_PIN, GPIO_PIN_RESET);
	HAL_Delay(5);
	HAL_GPIO_WritePin(RST_PORT, RST_PIN, GPIO_PIN_SET);
	__HAL_SPI_ENABLE(&hspi1);
	lcd_5110_command(lcd_init,6);
}

/*
 *
 */
void Turn_On_Backlight()
{
  HAL_GPIO_WritePin(LED_PORT, LED_PIN, GPIO_PIN_SET);
}

/*
 *
 */
void Turn_Off_Backlight()
{
  HAL_GPIO_WritePin(LED_PORT, LED_PIN, GPIO_PIN_RESET);
}

/*
 *
 */
void lcd_5110_SetCursor(uint8_t row,uint8_t line)
{
   uint8_t position[2] = {0};


   position[0] = (X_Base_Address | row);
   position[1] = (Y_Base_Address | line);


   lcd_5110_command(position,2);
}

/*
 *
 */
void lcd_5110_Clear()
{
	uint8_t data[2],value[1];
  for(uint8_t y=0; y<6; y++)
	 {
	   data[0] = (Y_Base_Address | y);
	   //lcd_5110_command(data[0]);
	   for(uint8_t x=0; x<84; x++)
	      {
		     data[1]= (X_Base_Address | x);
		     lcd_5110_command(data,2);
		     value[0]=0x00;
		     lcd_5110_data(value,1);
	      }
	  }
}

/*
 *
 */
void lcd_5110_command(uint8_t *command,uint8_t length)
{
 // uint8_t Data_comand[1]= {0};
  //Data_comand[0] = command;
   HAL_GPIO_WritePin(DC_PORT, DC_PIN, GPIO_PIN_RESET);
   HAL_GPIO_WritePin(SCE_PORT, SCE_PIN, GPIO_PIN_RESET);
   HAL_SPI_Transmit(lcd_5510,&(*command),length,HAL_MAX_DELAY);
   HAL_GPIO_WritePin(SCE_PORT, SCE_PIN, GPIO_PIN_SET);
}

/*
 *
 */
void lcd_5110_data(uint8_t *Data, uint8_t length)
{
  //uint8_t Data_value[1] = {0};
  //Data_value[0] = Data;
  HAL_GPIO_WritePin(DC_PORT, DC_PIN, GPIO_PIN_SET);
  HAL_GPIO_WritePin(SCE_PORT, SCE_PIN, GPIO_PIN_RESET);
  HAL_SPI_Transmit(lcd_5510,&(*Data),length,HAL_MAX_DELAY);
  HAL_GPIO_WritePin(SCE_PORT, SCE_PIN, GPIO_PIN_SET);
}

